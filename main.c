#include <stdio.h> 
// biblioteca para impressão
int main() {
    // main = função principal
    int numero = 80;
    float numero2 = 3.6;
    double numero3 = 3.14569;
    // declarando variáveis
    printf("Numero: %d",numero);
    printf("Numero: %f", numero2);
    printf("Numero: %f", numero3);
    // imprimindo variáveis
    return 0;
}
// tipos de dados: int, char, float, double 
// int: inteiros, char: caracteres, float: ponto flutuante, double: ponto flutuante com precisão maior